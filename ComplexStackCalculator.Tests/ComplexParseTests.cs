namespace ComplexStackCalculator.Tests;

public class ComplexParseTests
// Класс с тестами методов Parse и TryParse
{
    [Theory]
    // Атрибут xUnit для параметризованного теста

    [InlineData("3+4i", 3, 4)]
    // Строка "3+4i" должна преобразоваться в 3 + 4i

    [InlineData("3-4i", 3, -4)]
    // Строка "3-4i" должна преобразоваться в 3 - 4i

    [InlineData("4i", 0, 4)]
    // Строка "4i" должна преобразоваться в 0 + 4i

    [InlineData("-i", 0, -1)]
    // Строка "-i" должна преобразоваться в 0 - 1i

    [InlineData("5", 5, 0)]
    // Строка "5" должна преобразоваться в 5 + 0i

    [InlineData("(3,4)", 3, 4)]
    // Формат "(3,4)" должен преобразоваться в 3 + 4i

    [InlineData("(3;4)", 3, 4)]
    // Формат "(3;4)" тоже должен преобразоваться в 3 + 4i

    [InlineData("0.5+2.1i", 0.5, 2.1)]
    // Проверка работы с дробными числами

    public void Parse_ValidFormats_Works(string s, double re, double im)
    // Тест проверки корректного парсинга допустимых строк
    {
        var z = Cx.Parse(s);
        // Преобразуем строку в комплексное число

        Assert.True(z.AlmostEquals(new Cx(re, im), 1e-12));
        // Проверяем, что результат совпадает с ожидаемым значением
        // с точностью 10^-12
    }

    [Theory]
    // Параметризованный тест для неверных строк

    [InlineData("")]
    // Пустая строка

    [InlineData("abc")]
    // Некорректный текст

    [InlineData("3++4i")]
    // Ошибка в записи числа

    [InlineData("i3")]
    // Неправильный формат мнимой части

    [InlineData("3+4")]
    // Нет символа i у мнимой части

    public void TryParse_Invalid_ReturnsFalse(string s)
    // Тест проверки, что неверные строки не парсятся
    {
        bool ok = Cx.TryParse(s, out _);
        // Пытаемся преобразовать строку в комплексное число
        // out _ означает, что результат нам не нужен

        Assert.False(ok);
        // Проверяем, что метод вернул false
    }
}