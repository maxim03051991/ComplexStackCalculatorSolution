using ComplexNumbersLib;   // Тип Cx (комплексные числа)
using System.Numerics;     // Числовые типы (.NET Complex и др.)

namespace ComplexStackCalculator.Operations;

// Интерфейс (контракт) для любой операции калькулятора.
// Любая операция должна иметь:
// 1) ключ (название/символ)
// 2) арность (1 или 2)
// 3) метод выполнения
public interface ICalcOperation
{
    // Ключ операции, по которому её находят (например "+", "-", "sin")
    string Key { get; }

    // Арность: сколько аргументов нужно операции (1 или 2)
    int Arity { get; }

    // Выполнить операцию.
    // a — первый аргумент (обязательный)
    // b — второй аргумент (для бинарных операций), может быть null для унарных
    // Возвращает результат операции как комплексное число Cx
    Cx Execute(Cx a, Cx? b = null);
}