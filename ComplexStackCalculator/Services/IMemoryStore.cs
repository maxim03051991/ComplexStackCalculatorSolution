
namespace ComplexStackCalculator.Services;

// Интерфейс (контракт) для хранения значения в памяти.
// <T> — обобщённый тип (generic), поэтому можно хранить любой тип данных:
// int, double, Complex, string и т.д.
public interface IMemoryStore<T>
{
    // Свойство, которое показывает есть ли значение в памяти.
    // Тип bool: true — значение есть, false — памяти пустая.
    // { get; } означает, что свойство доступно только для чтения.
    bool HasValue { get; }

    // Метод сохранения значения в память.
    // value — значение типа T, которое нужно сохранить.
    void Save(T value);

    // Метод попытки загрузить значение из памяти.
    // out T value — параметр, через который метод возвращает значение.
    // Метод возвращает:
    // true  — если значение было успешно получено
    // false — если память пустая.
    bool TryLoad(out T value);

    // Метод очистки памяти.
    // После вызова этого метода значение удаляется.
    void Clear();
}