using System.Collections.Generic;

namespace ComplexStackCalculator.Services;

// Интерфейс стека.
// <T> — обобщённый тип, поэтому стек может хранить любые типы данных.
public interface IStackService<T>
{
    // Свойство возвращает количество элементов в стеке.
    // Только для чтения.
    int Count { get; }

    // Возвращает список элементов стека только для чтения.
    // Элементы идут в порядке:
    // index 0 — верх стека (top)
    // index 1 — следующий элемент
    // index 2 — следующий и т.д.
    IReadOnlyList<T> ItemsTopFirst { get; }

    // Добавляет новый элемент в верх стека.
    void Push(T value);

    // Удаляет верхний элемент стека и возвращает его.
    T Pop();

    // Возвращает верхний элемент стека,
    // но не удаляет его.
    T Peek();

    // Полностью очищает стек.
    void Clear();
}